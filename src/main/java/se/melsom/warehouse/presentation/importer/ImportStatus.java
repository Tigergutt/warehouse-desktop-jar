package se.melsom.warehouse.presentation.importer;

/**
 * The enum Import status.
 */
public enum ImportStatus {
    /**
     * Pending import status.
     */
    PENDING,
    /**
     * Ignored import status.
     */
    IGNORED,
    /**
     * Valid import status.
     */
    VALID,
    /**
     * Ok import status.
     */
    OK,
    /**
     * Warning import status.
     */
    WARNING,
    /**
     * Error import status.
     */
    ERROR
}
